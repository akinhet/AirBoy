void drawText(int x, int y, int scale, uint16_t color, const char *str, uint16_t *buffer)
{
   const int fontw = 6,
      fonth = 8;

   if (y + fonth*scale > LCD_HEIGHT || y < 0 || x < 0 || scale < 1)
      return;

   int len = strlen(str),
      index = 0;

   for (int i = 0; i < len; i++) {
      if (x + i*fontw*scale > LCD_WIDTH)
         return;

      if (str[i] < ' ')
         return;

      index = str[i] - ' ';

      for (int ty = 0; ty < fonth*scale; ty++)
         for (int tx = 0; tx < fontw*scale; tx++) {
            if (defaultFont[index][fontw * (int)(ty/scale) + (int)(tx/scale)] == 1)
               buffer[ LCD_WIDTH * (y + ty) + x + i*fontw*scale + tx] = color;
            /* buffer[ LCD_WIDTH * (y + ty) + i*8*scale + tx] = defaultFont[index][8 * (int)(ty/scale) + (int)(tx/scale)] == 0 ? color : 0xFFFE; */
         }
   }
}
